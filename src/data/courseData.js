// 获取指定周次和日期的课程
export const getCourseForWeek = (dayIndex, period, weekNumber) => {
  const courseKey = `${dayIndex}-${period}`;
  const course = courseData[courseKey];
  
  if (!course) return null;
  
  // 检查当前周次是否在课程的周次范围内
  if (!course.weeks.includes(weekNumber)) return null;
  
  return {
    name: course.name,
    location: course.location,
    teacher: course.teacher
  };
};

// 处理特殊课程（如单双周、特定周次）
